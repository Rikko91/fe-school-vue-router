<template>
  <div
    v-if="user"
    class="container">
    <h1 class="title is-1">{{ user.name }}</h1>
    <div class="tabs">
      <ul class="user-menu">
        <router-link
          tag="li"
          exact
          :to="'/users/' + user.id">
          <a href="">Profile</a>
        </router-link>
        <router-link
          tag="li"
          :to="'/users/' + user.id + '/posts/'">
          <a href="">Posts</a>
        </router-link>
      </ul>
    </div>
    <router-view/>
  </div>
</template>

<script lang="ts">
  import Vue from 'vue';
  import {IUser} from '../common/interfaces/IUser';
  import {UserService} from '../common/services/UserService';

  interface IUserData {
    user: IUser | null;
  }

  export default Vue.extend({
    data(): IUserData {
      return {
        user: null
      };
    },
    created() {
      UserService.getUserById(this.$route.params.userId)
        .then(user => {
          this.user = user;
        });
    }
  });
</script>

<style lang="scss" scoped>
  .user-menu li {
    margin-top: 0;
  }
</style>